package Calculadora;

public class Calculadora 
{
	//Soma de inteiros
	public int somar(int x, int y)
	{
		return(x+y);
	}
	
	//Soma de inteiros
	public float somar (float x, float y)
	{
		return(x+y);
	}

	//Soma de strings
	public String somar (String x, String y)
	{
		return (x+y);
	}
	
	//Soma de 3 float
	public float somar (float x, float y, float z)
	{
		return (x+y+z);
	}
}
